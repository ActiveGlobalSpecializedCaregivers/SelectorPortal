package com.cloudaxis.agsc.portal.model;

public class CandidateStatusAmount {

	private Integer totalAmount;
	
	private Integer newAmount;
	
	private Integer contractedAmount;
	
	private Integer taggedAmount;
	
	private Integer availableAmount;
	
	private Integer onHoldAmount;

	
	public Integer getTotalAmount() {
		return totalAmount;
	}

	public void setTotalAmount(Integer totalAmount) {
		this.totalAmount = totalAmount;
	}

	public Integer getNewAmount() {
		return newAmount;
	}

	public void setNewAmount(Integer newAmount) {
		this.newAmount = newAmount;
	}

	public Integer getContractedAmount() {
		return contractedAmount;
	}

	public void setContractedAmount(Integer contractedAmount) {
		this.contractedAmount = contractedAmount;
	}

	public Integer getTaggedAmount() {
		return taggedAmount;
	}

	public void setTaggedAmount(Integer taggedAmount) {
		this.taggedAmount = taggedAmount;
	}

	public Integer getAvailableAmount() {
		return availableAmount;
	}

	public void setAvailableAmount(Integer availableAmount) {
		this.availableAmount = availableAmount;
	}

	public Integer getOnHoldAmount() {
		return onHoldAmount;
	}

	public void setOnHoldAmount(Integer onHoldAmount) {
		this.onHoldAmount = onHoldAmount;
	}
	
	
}
